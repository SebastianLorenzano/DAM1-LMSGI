document.write("Bucle hecho con <b>WHILE</b><br>")
let i = 0
while (i < 10) {
    document.write("Que llegue ya <u>Semana Santa,</u> ¡por favor!<br>");
    i++;
}
document.write("¿Ha quedado claro?<br><br>")
